package breaker;

public class Rect {
	public Rect()
	{
		this.x = 0;
		this.y = 0;
		this.w = 0;
		this.h = 0;
	}
	
	public Rect(int x, int y, int w, int h)
	{
		this.x = x;
		this.y = y;
		this.w = w;
		this.h = h;
	}
	
	public int x = 0;
	public int y = 0;
	public int w = 0;
	public int h = 0;
}
